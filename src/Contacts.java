public class Contacts {
    String f_name;
    String l_name;
    String address;
    String city;
    String state;
    int zip;
    int phn_no;
    String email;

    public Contacts(String f_name, String l_name, String address, String city, String state, int zip, int phn_no, String email) {
        this.f_name = f_name;
        this.l_name = l_name;
        this.address = address;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.phn_no = phn_no;
        this.email = email;
    }


}
